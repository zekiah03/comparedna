export function analogyUserPrompt(e: Entry): string {
  const axesList = (Object.keys(AXIS_META) as (keyof typeof AXIS_META)[])
    .map(k => `${k}(${AXIS_META[k].label})=${e.axes12[k]}`)
    .join(", ");

  const layers = e.layers
    ? `
- 由来: ${e.layers.origin}
- 挙動: ${e.layers.behavior}
- 軌跡: ${e.layers.trajectory}
- 感情: 恐怖=${e.layers.emotions.fear} / 喜び=${e.layers.emotions.joy} / 愛=${e.layers.emotions.love}`
    : "";

  return `対象: 「${e.name}」

キャッチ: ${e.catchphrase}
要約: ${e.summary}
タイプ: ${e.type} / カテゴリ: ${e.category}
12軸: ${axesList}${layers}

この対象を一人の人間に翻訳し、履歴書をスキーマ通り生成してください。
- 年齢は対象の寿命ステージに忠実に
- 氏名・性別・出身・家庭・職業は自由に
- 学歴と職歴は簡潔な文章で (年月の羅列はしない)
- 自己PRは本人の口調を固定した一人称で
- 凡庸な模範人生にしない、人間味のある選択を`;
}
